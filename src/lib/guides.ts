const byRewardDesc = (a: Card, b: Card) =>
  (b.base_reward_rate ?? 0) - (a.base_reward_rate ?? 0) || a.annual_fee - b.annual_fee;
const byFeeAsc = (a: Card, b: Card) =>
  a.annual_fee - b.annual_fee || (b.base_reward_rate ?? 0) - (a.base_reward_rate ?? 0);

